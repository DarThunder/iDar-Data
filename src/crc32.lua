function crc32.crc32(str)
    local band, bxor, rshift = bit.band, bit.bxor, bit.rshift
    local crc=0xFFFFFFFF;

	for i = 1, #str do
		local byte = string.byte(str, i)
		local bit_index = band(bxor(crc, byte), 0xFF) + 1
        local table_entry = crc_table[bit_index]
        crc = bxor(rshift(crc, 8), table_entry)
	end

	return bit.bnot(crc);
end

return crc32